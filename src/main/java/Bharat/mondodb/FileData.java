package Bharat.mondodb;

public class FileData {
	String filename;
	String archiveid;
	String status;
	String sharedwith;
	String filesize;
	String fileuploaddate;
	
	/**
	 * @return the filename
	 */
	public String getFilename() {
		return filename;
	}
	/**
	 * @param filename the filename to set
	 */
	public void setFilename(String filename) {
		this.filename = filename;
	}
	/**
	 * @return the archiveid
	 */
	public String getArchiveid() {
		return archiveid;
	}
	/**
	 * @param archiveid the archiveid to set
	 */
	public void setArchiveid(String archiveid) {
		this.archiveid = archiveid;
	}
	/**
	 * @return the status
	 */
	public String getStatus() {
		return status;
	}
	/**
	 * @param status the status to set
	 */
	public void setStatus(String status) {
		this.status = status;
	}
	/**
	 * @return the sharedwith
	 */
	public String getSharedwith() {
		return sharedwith;
	}
	/**
	 * @param sharedwith the sharedwith to set
	 */
	public void setSharedwith(String sharedwith) {
		this.sharedwith = sharedwith;
	}
	/**
	 * @return the filesize
	 */
	public String getFilesize() {
		return filesize;
	}
	/**
	 * @param filesize the filesize to set
	 */
	public void setFilesize(String filesize) {
		this.filesize = filesize;
	}
	/**
	 * @return the fileuploaddate
	 */
	public String getFileuploaddate() {
		return fileuploaddate;
	}
	/**
	 * @param fileuploaddate the fileuploaddate to set
	 */
	public void setFileuploaddate(String fileuploaddate) {
		this.fileuploaddate = fileuploaddate;
	}

}
